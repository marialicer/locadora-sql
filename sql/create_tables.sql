CREATE TABLE CLIENTE (
    CODCLIENTE NUMBER (10) NOT NULL,
    NOMCLIENTE VARCHAR2(50) NOT NULL,
    ENDCLIENTE VARCHAR2(50) NULL,
    BAIRCLIENTE VARCHAR2 (20) NULL,
    CIDCLIENTE VARCHAR2 (50) NULL,
    UFCLIENTE CHAR (2) NULL,
    CONSTRAINT PK_CLIENTE PRIMARY KEY (CODCLIENTE)
);

CREATE TABLE PRECO (
    COR CHAR(2) NOT NULL,
    VALOR NUMBER (10,2) NOT NULL,
    CONSTRAINT PK_PRECO PRIMARY KEY (COR)
);

CREATE TABLE GENERO (
    CODGENERO NUMBER (3) NOT NULL,
    DESCRICAO VARCHAR2 (20) NOT NULL,
    CONSTRAINT PK_GENERO PRIMARY KEY (CODGENERO)
);

CREATE TABLE FILME (
    CODFILME NUMBER(3) NOT NULL,
    NOMEFILME VARCHAR(50) NOT NULL,
    COR CHAR(2) NOT NULL,
    STATUS CHAR(1) NOT NULL,
    CODGENERO NUMBER(3) NOT NULL,
    CONSTRAINT PK_FILME PRIMARY KEY (CODFILME),
    CONSTRAINT FK_FILME_PRECO FOREIGN KEY (COR) REFERENCES PRECO (COR),
    CONSTRAINT FK_FILME_GENERO FOREIGN KEY (CODGENERO) REFERENCES GENERO (CODGENERO)
);

CREATE TABLE LOCACAO (
    CODLOCACAO NUMBER GENERATED BY DEFAULT AS IDENTITY,
    CODCLIENTE NUMBER (10) NOT NULL,
    DATALOC DATE NOT NULL,
    CODFILME NUMBER (3) NOT NULL,
    DATADEVOLUCAO DATE NULL,
    CONSTRAINT PK_LOCACAO PRIMARY KEY (CODLOCACAO),
    CONSTRAINT FK_CLIENTE_LOCACAO FOREIGN KEY (CODCLIENTE) REFERENCES CLIENTE (CODCLIENTE),
    CONSTRAINT FK_LOCACAO_FILME FOREIGN KEY (CODFILME) REFERENCES FILME (CODFILME)
);